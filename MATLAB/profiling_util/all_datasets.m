%==========================================================================
% This script is used to generate a list of all data sets.
%==========================================================================

function [names, paths] = all_datasets
    % All data sets are assumed to have a CSV extension
    % Note: Data sets are listed here in increasing order of size, so that the 
    % script produces as many results as quickly as possible. The command used 
    % to do this is `du *.csv | sort -n | awk '{print $2}'`.
    data_dir   = strcat('.', filesep, 'Datasets');
    names = {
        'testoutrank', ...
        'ball1', ...
        'testCD', ...
        'runningex1k', ...
        'testCDST2', ...
        'testCDST3', ...
        'testCDST', ...
        'runningex10k', ...
        'runningex20k', ...
        'segmentation', ...
        'runningex30k', ...
        'pendigits', ...
        'runningex40k', ...
        'spam_train', ...
        'runningex50k', ...
        'spam', ...
        'letter-recognition', ...
        'mesh_network', ...
        'magicgamma', ...
        'musk', ...
        'connect4', ...
    };

    paths = strcat(data_dir, filesep, names(:), '.csv');
